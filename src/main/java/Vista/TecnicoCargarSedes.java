    private void btnCargarSedeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarSedeActionPerformed
        msjConfirmacion.setText("Verifique que haya ingresado datos validos");
        String barrio = txtBarrio.getText().toString();
        String membresia = comboMembresia.getSelectedItem().toString();
        String emplazamiento = comboEmplazamiento.getSelectedItem().toString();
        double superficie = Double.parseDouble(txtSuperficie.getText());
        int alquiler = Integer.parseInt(txtAlquiler.getText());
        if (controlador.altaSucursal(barrio, membresia, emplazamiento, superficie, alquiler)) {
            msjConfirmacion.setText("La sede " + barrio + " fue cargada exitosamente");
        } else {
            msjConfirmacion.setText("Ya hay una sede para el barrio " + barrio);
        }
    }//GEN-LAST:event_btnCargarSedeActionPerformed

